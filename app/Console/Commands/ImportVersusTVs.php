    private function importTV(array $tvData): void
    {
        $tv = Product::where('name', $tvData['name'])
            ->where('product_type_id', 8)
            ->first();
            
        if (!$tv) {
            $tv = Product::create([
                'name' => $tvData['name'],
                'brand' => $tvData['brand'],
                'product_type_id' => 8,
                'description' => $tvData['description'],
                'image_url' => 'https://ui-avatars.com/api/?name=' . urlencode($tvData['name']) . '&size=400&background=1f2937&color=fff',
                'source_url' => $tvData['source_url'],
            ]);
        }
        
        $tv->specValues()->delete();
        
        foreach ($tvData['specs'] as $specName => $specValue) {
            $this->addSpecification($tv, $specName, $specValue);
        }
    }
    
    private function addSpecification(Product $product, string $specName, string $specValue): void
    {
        $specKey = $this->getOrCreateSpecKey($specName);
        
        if (is_numeric($specValue)) {
            SpecValue::create([
                'product_id' => $product->id,
                'spec_key_id' => $specKey->id,
                'value_number' => (float)$specValue,
            ]);
        } elseif (in_array(strtolower($specValue), ['yes', 'no', 'da', 'nu'])) {
            SpecValue::create([
                'product_id' => $product->id,
                'spec_key_id' => $specKey->id,
                'value_bool' => in_array(strtolower($specValue), ['yes', 'da']),
            ]);
        } else {
            SpecValue::create([
                'product_id' => $product->id,
                'spec_key_id' => $specKey->id,
                'value_string' => $specValue,
            ]);
        }
    }
    
    private function getOrCreateSpecKey(string $name): SpecKey
    {
        // Traducem numele Ã®n romÃ¢nÄƒ
        $translatedName = $this->translateSpecName($name);
        
        if (isset($this->specKeyCache[$translatedName])) {
            return $this->specKeyCache[$translatedName];
        }
        
        $specKey = SpecKey::where('name', $translatedName)
            ->where('product_type_id', 8)
            ->first();
            
        if (!$specKey) {
            $specKey = SpecKey::create([
                'name' => $translatedName,
                'slug' => Str::slug($translatedName),
                'product_type_id' => 8,
